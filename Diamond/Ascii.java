package sangamone_pack;

public class Ascii {

	public static void main(String[] args) {
		int num1=65;
		for(int i=0;i<=25;i++)
		{
			System.out.print((char)(num1+i));
		}
		System.out.println();
		int num2=97;
		for(int i=0;i<=25;i++)
		{
			System.out.print((char)(num2+i));
		}
		System.out.println();
		char ch1='A';
		System.out.println((int)ch1);
	}

}
